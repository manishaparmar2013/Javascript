/*
This function takes changes to the country dropdown and modifies the state choices based on an object. 
it is called like so:

var country = document.getElementById("FIELD_"+FieldIDs["ShippingCountry"]);
var state = document.getElementById("FIELD_"+FieldIDs["ShippingState"]);
		
function StorefrontEvaluateFieldsHook(field){
		if (field.id == country.id){
			SBC.setDropdown(country,SBC.statesByCountry,state);
		}
}		

sel = the master dropdown field (country in this case)
data = the object variable containing the dropdown data (state by country information)
dependentSel = the dependent dropdown field (state in this case)

*/		
SBC.setDropdown = function(sel,data,dependentSel) {
	//setup selection value and object choice
	var selection = sel.options[sel.selectedIndex].value;
	var arrOptions = data[selection];
	var opt;
	//remove existing options from previous choice
	while (dependentSel.hasChildNodes()) {
		dependentSel.removeChild(dependentSel.childNodes[0]);
		dependentSel.childNodes[0] = null;
	}
	//insert default empty option
	var defOpt = new Option('- Select -','');
	//append the default option to the list (should be first but just in case)
			try {
				dependentSel.add(defOpt,null);
			} catch (e) {
				//for IE7 and earlier
				if (e.name == "TypeError") {
					dependentSel.options[dependentSel.options.length] = defOpt;
				} else {
					throw e;
				}
			}
	//add all appropriate options from object
	for (var i in arrOptions) {
			opt = new Option(arrOptions[i].display,arrOptions[i].value);
			//append the option to the list
			try {
				dependentSel.add(opt,null);
			} catch (e) {
				//for IE7 and earlier
				if (e.name == "TypeError") {
					dependentSel.options[dependentSel.options.length] = opt;
				} else {
					throw e;
				}
			}	
	}
}